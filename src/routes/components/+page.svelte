<div class="section">
<h2 id="overview">Installation</h2>
<p>Import the Narramancer Unity package into your project</p>
</div>

<div class="section">
<h2 id="overview">Set Api Settings</h2>
<p>Because Narramancer depends on Odin Serializer, the .NET Api for your project must be set to 4.x.</p>
<p>Go to Project Settings -> Player -> Other Settings and Change Api Compatibility Level to “.NET 4.x”</p>
</div>

<div class="section">
<h2 id="overview">Initialization</h2>
<p>Before using Narrmanacer for the first time, you must trigger initalization by opening the Narramancer window.</p>
<p>Open the Narramancer Window by choose ‘Window -> Narramancer’ from the menu bar.</p>
</div>


